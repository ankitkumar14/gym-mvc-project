package com.ankit.model;

public class Slotdash {

	private Integer slot_no;
	private String start_time;
	private String end_time;
	private Integer no_of_stud;
	public Slotdash() {
		super();
	}
	public Integer getSlot_no() {
		return slot_no;
	}
	public void setSlot_no(Integer slot_no) {
		this.slot_no = slot_no;
	}
	public String getStart_time() {
		return start_time;
	}
	public void setStart_time(String start_time) {
		this.start_time = start_time;
	}
	public String getEnd_time() {
		return end_time;
	}
	public void setEnd_time(String end_time) {
		this.end_time = end_time;
	}
	public Integer getNo_of_stud() {
		return no_of_stud;
	}
	public void setNo_of_stud(Integer no_of_stud) {
		this.no_of_stud = no_of_stud;
	}
	
}
